                <div id="Overview" class="small-12" data-magellan-target="Overview">
                    <h2>Overview</h2>
                    <p>
                        On a given night during the long eighteenth century (1660-1800), hundreds of spectators gathered
                        to participate in an institution at the heart of public life in London: the theater. An evening
                        at the playhouse was a far more elaborate event than we might assume today, consisting not just
                        of full-length dramatic or operatic performances, but also myriad entertainments interspersed
                        between the acts: prologues, epilogues, afterpieces, pantomimes, instrumental and vocal music,
                        dancing, and more.
                    </p>
                    <p>
                        The London Stage Database gathers together the surviving records of nearly 52,000 such
                        performance events, making it possible to browse and filter these events by date, location,
                        titles of plays and entertainments staged, and actors and roles represented on cast lists. This
                        database sheds light on economic, political, social, and cultural developments extending far
                        beyond the playhouse.
                    </p>
                    <p>
                        We hope that this database, like the eighteenth-century London theater itself, will capture the
                        imagination of a broad and varied public, including scholars, students, librarians, archivists,
                        theater practitioners, data scientists, and genealogists.
                    </p>
