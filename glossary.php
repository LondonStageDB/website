            <p>
            <p>Users less familiar with English theater of the long eighteenth century (1660-1800) may run across a few
                unfamiliar words in using the site. Here is a brief glossary of some of the most frequently used
                terms:</p>
            <h2 id='mainpiece'>Mainpiece</h2>
            <p>The main play, usually five acts long, that was given top billing for an evening at the theater. It was
                often followed by at least one <a href='/glossary.php#afterpiece'>afterpiece</a>, and interspersed with
                a variety of musical pieces, dances, monologues, and/or <a href='/glossary.php#entertainment'>entertainments</a>
                between the acts.</p>
            <h2 id='afterpiece'>Afterpiece</h2>
            <p>A shorter (often one- or two-act) dramatic piece meant to be performed after the <a
                        href='/glossary.php#mainpiece'>mainpiece</a>; usually comical or farcical in nature.</p>
            <h2 id='entertainment'>Entertainment</h2>
            <p>An interlude or other performance piece that might feature in an evening at the theater. Popular forms of
                entertainment throughout the century included not only singing, dancing, and musical performances, but
                also acrobatics, imitations or impersonations, and acts featuring trained animals.</p>
            <h2 id='pantomime'>Pantomime</h2>
            <p>An English form of performance, sometimes referred to as "Harlequinade," that was adapted from the
                Italian <i>commedia dell'arte</i> tradition and featured stock characters like Harlequin, Columbine,
                Scaramouche, and Pantaloon. These performances incorporated dance, gesture, slapstick physical comedy,
                elaborate scenery, and special effects.</p>
            <h2 id='prologue-epilogue'>Prologue/Epilogue</h2>
            <p>A short framing piece, usually in poetic verse, that preceded (prologue) or followed (epilogue) the <a
                        href='/glossary.php#mainpiece'>mainpiece</a>. These pieces could be performed by actors from the
                play either in character or as themselves, or by other conventional stage figures. Some were written as
                companion pieces for specific plays, while others were more general and could accompany a variety of
                plays. Prologues and epilogues alike were most commonly spoken on the first few nights of a new play's
                run and attempted to win the audience's support and applause for the new offering.</p>
            <h2 id='benefit'>Benefit</h2>
            <p>A performance for which the profits would be assigned to a specific actor, playwright, or charitable
                cause. Playwrights' benefits typically took place every third night of the first run of a new play; a
                play that lasted six or nine consecutive nights was considered successful. Star actors' contracts
                usually included the right to one or more benefit nights, usually in the spring, which made up a
                significant portion of their earnings for the year.</p>
            <h2 id='repertory'>Repertory</h2>
            <p>During this period, the major London playhouses were repertory theaters. This means that they had a
                "stock" of dozens of plays that they performed in rotation throughout a theatrical <a
                        href='/glossary.php#season'>season</a>. The repertory system required that actors have their
                parts for numerous scripts memorized and ready to go on short notice.</p>
            <h2 id='restoration'>Restoration</h2>
            <p>The return of the monarch Charles II to the English throne in 1660 following the English civil wars. The
                Restoration is considered an important event in English theatrical history because King Charles reopened
                the public theaters in London, which had been closed since 1642, and decreed that women could now
                perform on the public stage—a practice that previously had been forbidden in England.</p>
            <h2 id='season'>Season</h2>
            <p>At this time, the London theaters operated on a seasonal basis, closing during the summer months when
                many well-off Londoners left the city for vacation. Companies usually began the performance season in
                late summer or early fall and continued through the spring of the following year, although different
                theaters might begin and end their seasons on different dates. Importantly, certain kinds of performance
                (especially puppet shows) continued at the summer fairs, and the companies might also use the summer
                months to train up young actors.</p>
            <h2 id='accountbooks'>Account Books</h2>
            <p>In addition to information from printed advertisements for plays and from playgoers' diaries, the editors
                of <i>The London Stage, 1660-1800</i> rely on the financial records kept by the treasurers at the major
                theaters. Where available, total box office receipts for an evening are often listed in pounds,
                shillings, and pence (e.g. £57 14s. 6d.) in the "Comment" for an event. For entries from Part 5
                (1776-1800), additional numbers sometimes follow in parentheses, breaking down the total revenue into
                income from different types of tickets: "First Account" tickets sold to people who were in the house at
                the rise of the curtain; "Half Price" or "Second Account" tickets sold at reduced prices for admission
                after the conclusion of the third act; and "After-Money," income from late-comers after the First and
                Second Accounts for the evening had already been entered. For entries taken from Part 5, events at Drury
                Lane list the total receipts first, followed by these three categories; in the same volume, for events
                at Covent Garden, the total receipts are listed first, followed by the sum of First Account and Half
                Price tickets, followed by the After-Money. So, for example, if the comment for a performance at Drury
                Lane reads "Receipts £174 7s. 6d. (132/8/0; 36/2/6; 5/17/0)," it means that the theater took in £132 8s.
                for full-price admissions to the entire evening's entertainment, £36 2s. 6d. for half-price tickets to
                everything after the third act of the <a href='/glossary.php#mainpiece'>mainpiece</a>, and £5 17s. in
                reduced-price tickets for very late admissions to the show.</p>
            </p>
