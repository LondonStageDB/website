                        <h3 id='restoration'>Restoration</h3>
                        <p>The return of the monarch Charles II to the English throne in 1660 following the English
                            civil wars. The Restoration is considered an important event in English theatrical
                            history because King Charles reopened the public theaters in London, which had been
                            closed since 1642, and decreed that women could now perform on the public stage—a
                            practice that previously had been forbidden in England.
                        </p>
                        <h3 id="Long18">Long Eighteenth Century</h3>
                        <p>The period stretching from the reopening of the public playhouses following the English
                            civil wars (1660) to the end of the eighteenth century (1800)</p>
